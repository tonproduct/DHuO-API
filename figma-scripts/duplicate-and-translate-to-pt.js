const selected = figma.currentPage.selection[0];
if (!selected) { figma.notify('Selecione um frame.', { error: true }); return; }

// Duplica o frame
const clone = selected.clone();
clone.x = selected.x + selected.width + 100;
clone.name = selected.name + ' (PT)';
figma.currentPage.selection = [clone];
figma.viewport.scrollAndZoomIntoView([clone]);

const textNodes = clone.findAll(n => n.type === 'TEXT');
let count = 0;
let errors = 0;

for (const node of textNodes) {
  const translated = translations[node.characters];
  if (!translated) continue;

  try {
    const len = node.characters.length;
    const fontSet = new Set();
    for (let i = 0; i < len; i++) {
      fontSet.add(JSON.stringify(node.getRangeFontName(i, i + 1)));
    }
    for (const fStr of fontSet) {
      await figma.loadFontAsync(JSON.parse(fStr));
    }

    const firstFont  = node.getRangeFontName(0, 1);
    const firstSize  = node.getRangeFontSize(0, 1);
    const firstFills = node.getRangeFills(0, 1);

    node.characters = translated;
    node.setRangeFontName(0, translated.length, firstFont);
    node.setRangeFontSize(0, translated.length, firstSize);
    node.setRangeFills(0, translated.length, firstFills);

    count++;
  } catch (e) {
    errors++;
  }
}

// Detecta textos que parecem inglês (só ASCII, 2+ chars, não são números/símbolos puros)
const enPattern = /^[A-Za-z][A-Za-z0-9 .,!?'"\-+()@#%_:/\\]*$/;
const ptValues = new Set(Object.values(translations));
const missed = textNodes
  .filter(n => !translations[n.characters] && enPattern.test(n.characters) && n.characters.length > 1 && !ptValues.has(n.characters))
  .map(n => JSON.stringify(n.characters));

if (missed.length > 0) {
  const preview = missed.slice(0, 3).join(' | ');
  figma.notify(`Duplicado! ${count} traduzido(s). Faltam ${missed.length}: ${preview}`, { timeout: 15000 });
} else {
  figma.notify(`Duplicado! ${count} traduzido(s)${errors ? `, ${errors} erro(s)` : ''}. Tudo traduzido!`);
}
